
<?php
/**
  * Fungsi untuk menghitung luas persegi panjang
  * @param int $panjang variable input panjang persegi panjang 
  * @param int $lebar variable input lebar persegi panjang 
  * @param int $luas perhitungan luas, yaitu $panjang * $lebar
  * @author Moh. Rifqi Azizi
  */

  function luasPersegiPanjang($panjang, $lebar){
    $luas = $panjang * $lebar;
    return $luas;
  }
?> 
